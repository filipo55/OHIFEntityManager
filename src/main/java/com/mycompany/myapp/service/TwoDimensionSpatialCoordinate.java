package com.mycompany.myapp.service;

public class TwoDimensionSpatialCoordinate {

    protected int coordinateIndex;
    protected float x;
    protected float y;

    /**
     * Gets the value of the coordinateIndex property.
     *
     */
    public int getCoordinateIndex() {
        return coordinateIndex;
    }

    /**
     * Sets the value of the coordinateIndex property.
     */
    public void setCoordinateIndex(int value) {
        this.coordinateIndex = value;
    }

    /**
     * Gets the value of the x property.
     *
     *
     */
    public float getX() {
        return x;
    }

    /**
     * Sets the value of the x property.
     *
     */
    public void setX(float value) {
        this.x = value;
    }

    /**
     * Gets the value of the y property.
     *
     *
     */
    public float getY() {
        return y;
    }

    /**
     * Sets the value of the y property.
     *
     *
     */
    public void setY(float value) {
        this.y = value;
    }

}
